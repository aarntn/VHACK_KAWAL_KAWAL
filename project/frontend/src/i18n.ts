const dictionaries: Record<Locale, Dictionary> = { en, id, th, vi, tl, ms };

export const localeOptions: Array<{ code: Locale; labelKey: string }> = [
  { code: 'en', labelKey: 'lang.en' },
  { code: 'id', labelKey: 'lang.id' },
  { code: 'th', labelKey: 'lang.th' },
  { code: 'vi', labelKey: 'lang.vi' },
  { code: 'tl', labelKey: 'lang.tl' },
  { code: 'ms', labelKey: 'lang.ms' },
];

export const t = (locale: Locale, key: string, vars?: Record<string, string | number>): string => {
  const template = dictionaries[locale][key] ?? dictionaries.en[key] ?? key;
  if (!vars) {
    return template;
  }
  return Object.entries(vars).reduce(
    (acc, [name, value]) => acc.replaceAll(`{${name}}`, String(value)),
    template,
  );
};

const backendTextKeyByEnglish: Record<string, string> = {
  'Proceed with step-up verification (OTP/biometric).': 'backend.nextStep.stepUp',
  'Send case to manual review.': 'backend.nextStep.review',
  'Ask customer to contact support.': 'backend.nextStep.contactSupport',
  'Payment approved.': 'backend.message.approved',
  'Pending additional verification.': 'backend.message.pending',
  'Payment declined.': 'backend.message.declined',
};

export const localizeBackendText = (locale: Locale, text?: string): string | undefined => {
  if (!text) {
    return text;
  }
  const lookup = backendTextKeyByEnglish[text.trim()];
  if (!lookup) {
    return text;
  }
  return t(locale, lookup);
};
